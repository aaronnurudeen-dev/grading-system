def display_result(name, matric, score, grade):
    """
    Display the formatted student result
    """
    print("\n" + "="*50)
    print("STUDENT RESULT")
    print("="*50)
    print(f"Name: {name}")
    print(f"Matric Number: {matric}")
    print(f"Score: {score}")
    print(f"Grade: {grade}")
    print("="*50 + "\n")
